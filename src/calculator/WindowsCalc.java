    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // declaring the variable where the result will be stored and 
        //using cases will do the maths according with the operation chosed
        String answer;
        switch (operation){
            
            case "+":
                result = firstnum + Double.parseDouble(txtDisplay.getText());
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
            
            case "-":
                result = firstnum - Double.parseDouble(txtDisplay.getText());
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
            
            case "*":
                result = firstnum * Double.parseDouble(txtDisplay.getText());
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
           
            case "/":
                result = firstnum / Double.parseDouble(txtDisplay.getText());
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
            
            case "%":
                result=firstnum%Double.parseDouble(txtDisplay.getText());
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
                
            case "Sin":
                //converting to radians
                //using the java.lang.Math library
                result=java.lang.Math.sin(Math.toRadians(firstnum));
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;
                
            case "Cos":
                //converting to radians
                //using the java.lang.Math library
                result=java.lang.Math.cos(Math.toRadians(firstnum));
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;
            
            case "Tan":
                //converting to radians
                //using the java.lang.Math library
                result=java.lang.Math.tan(Math.toRadians(firstnum));
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;
            
            case "Exp":
                //converting to radians
                //using the java.lang.Math library
                result=java.lang.Math.exp(Math.toRadians(firstnum));
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;   
                
            case "Pow":
                //using the java.lang.Math library
                result=java.lang.Math.pow(firstnum,Double.parseDouble(txtDisplay.getText()));
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;  
             
            case "Pow3":
                //using the java.lang.Math library
                result=java.lang.Math.pow(firstnum,3);
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;    
            
            case "Pow2":
                //using the java.lang.Math library
                result=java.lang.Math.pow(firstnum,2);
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;   
                
            case "Log":
                //using the java.lang.Math library
                result=java.lang.Math.log10(firstnum);
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break; 
                
            case "Ln":
                //using the java.lang.Math library
                result=java.lang.Math.log(firstnum);
                answer=Double.toString(result);
                txtDisplay.setText(answer);
                break;     
                
            case "Divide1":
                //using the java.lang.Math library
                result=1/firstnum;
                answer = Double.toString(result);
                txtDisplay.setText(answer);
                break;
                
            
                
        }
    }                                        

